# Suma de N Primeros Numeros
cant = int(input( " Digite la cantidad a Sumar  "))
num = 0
suma = 0
while num <= cant:
    suma = suma + num
    num +=1
print("*****************")
print(f" La sumatoria de los Primeros N NÃºmeros es: {suma}")
